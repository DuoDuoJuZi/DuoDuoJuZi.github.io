const ThemeManager = (() => {
  const KEY = "jz-theme";
  const html = document.documentElement;
  const body = document.body;
  const apply = (theme) => {
    html.setAttribute("data-theme", theme);
    body.setAttribute("data-theme", theme);
    localStorage.setItem(KEY, theme);
    if (typeof renderTravelMap === "function") {
      renderTravelMap(currentLang);
    }
  };
  const init = () => {
    const saved = localStorage.getItem(KEY);
    if (saved) {
      apply(saved);
      return;
    }
    const sys = window.matchMedia("(prefers-color-scheme: dark)").matches
      ? "dark"
      : "light";
    apply(sys);
  };
  const toggle = () => {
    const cur = html.getAttribute("data-theme");
    apply(cur === "dark" ? "light" : "dark");
  };
  window
    .matchMedia("(prefers-color-scheme: dark)")
    .addEventListener("change", ({ matches }) => {
      if (!localStorage.getItem(KEY)) apply(matches ? "dark" : "light");
    });
  return { init, toggle };
})();

function startTyped(lines) {
  const el = $("#typed-text");
  if (!el) return;
  if (typedInterval) {
    clearInterval(typedInterval);
    el.textContent = "";
  }
  let li = 0,
    ci = 0,
    deleting = false,
    pauseCount = 0;
  const PAUSE = 28;
  const SPEED_TYPE = 60,
    SPEED_DEL = 30;
  typedInterval = setInterval(
    () => {
      const line = lines[li];
      if (!deleting) {
        if (ci < line.length) {
          el.textContent = line.slice(0, ++ci);
        } else {
          if (++pauseCount < PAUSE) return;
          pauseCount = 0;
          deleting = true;
        }
      } else {
        if (ci > 0) {
          el.textContent = line.slice(0, --ci);
        } else {
          deleting = false;
          li = (li + 1) % lines.length;
        }
      }
    },
    deleting ? SPEED_DEL : SPEED_TYPE,
  );
}

