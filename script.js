function renderTimeline(lang) {
  const wrap = $("#timeline-list");
  if (!wrap) return;
  const data = I18N[lang].timeline_data;
  const COLS = 3, ROWS = 3;
  const nodeMap = {};
  TL_POSITIONS.forEach(([col, row], i) => {
    if (i < data.length) {
      const conn = TL_CONNECTORS[i] || { out:"", in:"" };
      nodeMap[`${col}-${row}`] = { item: data[i], idx: i, out: conn.out, tin: conn.in };
    }
  });

  const R = 32, C = 2 * Math.PI * R;

  let html = '<div class="tl-snake">';
  for (let r = 0; r < ROWS; r++) {
    html += '<div class="tl-row">';
    for (let c = 0; c < COLS; c++) {
      const key = `${c}-${r}`;
      const node = nodeMap[key];
      if (node) {
        const { item, idx, out, tin } = node;
        const outCls = out  ? `out-${out}`  : "";
        const inCls  = tin  ? `in-${tin}`   : "";

        html += `
          <div class="tl-cell ${outCls} ${inCls}" data-tl-idx="${idx}">
            <div class="tl-point ${item.active ? "active" : ""}">
              <div class="tl-dot"></div>
            </div>
            <div class="tl-label">
              <span class="tl-time">${item.time}</span>
              <span class="tl-title">${item.title}</span>
              <span class="tl-desc">${item.desc}</span>
            </div>
          </div>`;
      } else {
        html += `<div class="tl-cell tl-empty"></div>`;
      }
    }
    html += '</div>';
  }
  html += '</div>';
  wrap.innerHTML = html;
  wrap.querySelectorAll(".tl-cell[data-tl-idx]").forEach(el => {
    el.classList.add("tl-hidden");
  });
  initTimelineScroll(wrap, data.length);
}

function initTimelineScroll(wrap, total) {
  if (tlScrollUnlock) { tlScrollUnlock(); tlScrollUnlock = null; }

  let revealed = 0;
  let wheelBuffer = 0;
  const WHEEL_THRESHOLD = 50;
  let locked = false;

  const section = document.getElementById("timeline");
  const anchorCell = wrap.querySelector('.tl-cell[data-tl-idx="1"]');

  function isAnchorInView() {
    if (!anchorCell) return false;
    const rect = anchorCell.getBoundingClientRect();
    const vh = window.innerHeight;
    const center = rect.top + rect.height / 2;
    return center > vh * 0.1 && center < vh * 0.9;
  }

  function revealNode(idx) {
    const cell = wrap.querySelector(`.tl-cell[data-tl-idx="${idx}"]`);
    if (cell) {
      cell.classList.remove("tl-hidden");
      cell.classList.add("tl-visible");
    }
  }

  function revealNext() {
    if (revealed >= total) return;
    revealNode(revealed);
    revealed++;
    if (revealed >= total) {
      locked = false;
      cleanup();
    }
  }

  function cleanup() {
    if (tlScrollUnlock) { tlScrollUnlock(); tlScrollUnlock = null; }
  }

  function onWheel(e) {
    if (e.deltaY <= 0) return;

    if (!locked) {
      if (isAnchorInView() && revealed === 0) {
        locked = true;
        revealNext();
      }
      return;
    }

    e.preventDefault();

    if (!isAnchorInView()) {
      while (revealed < total) revealNext();
      return;
    }

    wheelBuffer += Math.abs(e.deltaY);
    if (wheelBuffer >= WHEEL_THRESHOLD) {
      wheelBuffer = 0;
      revealNext();
    }
  }

  window.addEventListener("wheel", onWheel, { passive: false });
  tlScrollUnlock = () => {
    window.removeEventListener("wheel", onWheel);
  };
}

