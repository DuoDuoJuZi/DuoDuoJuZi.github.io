function applyLang(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  localStorage.setItem("jz-lang", lang);
  const t = I18N[lang];
  document.documentElement.lang =
    lang === "zh" ? "zh-CN" : lang === "ja" ? "ja" : "en";
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    if (t[key] !== undefined) el.textContent = t[key];
  });
  document.title = t.title;
  startTyped(t.typed_lines);
  renderTimeline(lang);
  renderHobbies(lang);
  renderGames(lang);
  renderTravelMap(lang);
  $$(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });
}

async function detectLangByIP() {
  const saved = localStorage.getItem("jz-lang");
  if (saved) {
    applyLang(saved);
    return;
  }
  try {
    const res = await fetch("https://ipapi.co/json/", {
      signal: AbortSignal.timeout(4000),
    });
    const data = await res.json();
    const country = (data.country_code || "").toUpperCase();
    const langMap = { CN: "zh", TW: "zh", HK: "zh", JP: "ja" };
    applyLang(langMap[country] || "en");
  } catch {
    const bl = (navigator.language || "en").toLowerCase();
    if (bl.startsWith("zh")) applyLang("zh");
    else if (bl.startsWith("ja")) applyLang("ja");
    else applyLang("en");
  }
}

